// EXAMPLE 1 - Get the First and Last Elements of an Array in JavaScript

const arr = ['a', 'b', 'c', 'd'];

const first = arr[0];
console.log(first); // 👉️ a

const last = arr[arr.length - 1];
console.log(last); // 👉️ d

